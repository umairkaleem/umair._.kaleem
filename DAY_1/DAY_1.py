# comparison operators
x = 10
y = 20
print(x == y)  # False
print(x != y)  # True
print(x > y)   # False
print(x < y)   # True
print(x >= y)  # False
print(x <= y)  # True
# logical operators
print(x > 5 and y < 30)  # True
print(x < 5 or y < 30)   # True
print(not (x > 5))        # False
# identity operators
a = [1, 2, 3]
